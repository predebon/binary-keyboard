void scanButtons(){
	uint8_t bot0=0,bot1=0,backspace=0;
	printBits = HAL_GPIO_ReadPin(GPIOB, GPIO_PIN_8);
	bot0_prev = bot0;
	bot0 = !HAL_GPIO_ReadPin(zero_GPIO_Port, zero_Pin);
	bot1_prev = bot1;
	bot1 = !HAL_GPIO_ReadPin(one_GPIO_Port, one_Pin);
	backspace_prev = backspace;
	backspace = !HAL_GPIO_ReadPin(backspace_GPIO_Port, backspace_Pin);
	HAL_Delay(1);
	if (bot0 && bot1 && backspace && !bot0_prev && !bot1_prev && !backspace_prev)
		easterEgg();
	else if (bot0 && !bot0_prev){
		appendBit(0);
		while(bot0){
			bot0 = !HAL_GPIO_ReadPin(zero_GPIO_Port, zero_Pin);
			HAL_Delay(1);
		}
	}
	else if (bot1 && !bot1_prev){
		appendBit(1);
		while(bot1){
			bot1 = !HAL_GPIO_ReadPin(one_GPIO_Port, one_Pin);
			HAL_Delay(1);
		}
	}
	else if (backspace && !backspace_prev)
		resetValue();
	else
		return;
}
void resetValue(){
	if (printBits){
		for (int i=0; i<bitIndex; i++)
			sendChar(0x7F);
	}
	bitIndex=0;
	binVal=0;
}
void appendBit(uint8_t bit){
	binVal |= (bit<<(7-bitIndex));
	if(printBits)
		sendChar(bit+0x30);
	bitIndex++;
	if (bitIndex==8){
		sendChar(binVal);
		resetValue();
	}
}
void sendChar(uint8_t c){
	keyboardReport[0]=modifierTable[c];
	keyboardReport[2]=HIDTable[c];
	USBD_HID_SendReport(&hUsbDeviceFS, (uint8_t*)keyboardReport, sizeof(keyboardReport));
	HAL_Delay(20);
	keyboardReport[0]=0;
	keyboardReport[2]=0;
	USBD_HID_SendReport(&hUsbDeviceFS, (uint8_t*)keyboardReport, sizeof(keyboardReport));
	HAL_Delay(20);
}
void easterEgg(){
	  char devilsphrase[]="Hail satan!\n";
	  for (int i=0; i<strlen(devilsphrase); i++)
		    sendChar((uint8_t)devilsphrase[i]);
}
